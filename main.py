# 1-mashq
bor_a = lambda ism: "a" in ism

print(bor_a("Ruxshona"))
print(bor_a("Olim"))


# 2-mashq
bosh_harf = lambda s: s.upper()

print(bosh_harf("salom dunyo"))


# 3-mashq
farq_modul = lambda a, b: abs(a - b)

print(farq_modul(10, 4))


# 4-mashq
tengmi = lambda a, b: a == b

print(tengmi(5, 5))
print(tengmi(3, 7))


# 5-mashq
butun_qism = lambda n: n // 2

print(butun_qism(7))
print(butun_qism(10))
